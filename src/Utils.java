package src;

public class Utils {

    public static double Topla(double a,double b) {
        return  a + b;
    }

    public static double Cixma(double a,double b) {
        return  a - b;
    }

    public static double Vurma(double a,double b) {
        return  a * b;
    }

    public static double Bolme(double a,double b) {
        return  a / b;
    }

}
